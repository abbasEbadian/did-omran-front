const FAQPage = () => {
  const [openIndex, setOpenIndex] = useState<number | null>(null);

  const toggle = (i: number) => {
    setOpenIndex(openIndex === i ? null : i);
  };

  return (
    <div className="max-w-5xl mx-auto py-10 px-4">
      <h1 className="text-3xl font-bold text-center mb-6">سؤالات متداول</h1>
      {faqs.map((section, sectionIndex) => (
        <div key={section.category} className="mb-8">
          <h2 className="text-xl font-semibold mb-4 text-right border-dashed border-b-2 border-b-secondary700 pb-2">{section.category}</h2>
          <div className="space-y-4">
            {section.items.map((item, i) => {
              const index = parseInt(`${sectionIndex}${i}`);
              return (
                <div key={i} className="border border-did/15 rounded-2xl bg-white100">
                  <button
                    onClick={() => toggle(index)}
                    className="w-full text-right px-4 py-3 font-medium flex justify-between items-center hover:bg-did900 hover:text-white rounded-2xl transition"
                  >
                    <span>{item.question}</span>
                    <span>{openIndex === index ? '−' : '+'}</span>
                  </button>
                  {openIndex === index && (
                    <div className="px-4 pb-4 text-secondary text-sm leading-relaxed">{item.answer}</div>
                  )}
                </div>
              );
            })}
          </div>
        </div>
      ))}
    </div>
  );
};

export default FAQPage;
