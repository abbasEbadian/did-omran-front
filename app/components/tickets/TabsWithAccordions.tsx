// components/TabsWithAccordions.tsx
'use client';

import { useState } from 'react';
import AccordionTickets from './AccordionTickets';

export default function TabsWithAccordions() {
  const [activeTab, setActiveTab] = useState(0);

  return (
    <div className="mx-auto py-10">
      <div className="flex border-b mb-6">
        {faqTabs.map((tab, index) => (
          <button
            key={index}
            onClick={() => setActiveTab(index)}
            className={`px-4 py-2 font-medium text-sm transition border-b-2 ${
              activeTab === index
                ? 'border-blue-600 text-blue-600'
                : 'border-transparent text-gray-900 hover:text-blue-500'
            }`}
          >
            {tab.title}
          </button>
        ))}
      </div>

      <AccordionTickets items={faqTabs[activeTab].items} />
    </div>
  );
}

